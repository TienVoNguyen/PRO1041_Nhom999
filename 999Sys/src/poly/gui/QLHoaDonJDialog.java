    private void tblHoaDonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoaDonMouseClicked
        int index = tblHoaDon.getSelectedRow();
        if (index==-1) {
            return;
        }
        txtMaHD.setText(tblHoaDon.getValueAt(index, 0)+"");
        txtMaNV.setText(tblHoaDon.getValueAt(index, 2)+"");
        txtMaKH.setText(tblHoaDon.getValueAt(index, 3)+"");
        txtNgayMua.setText(tblHoaDon.getValueAt(index, 4)+"");
        txtGiamGia.setText(tblHoaDon.getValueAt(index, 5)+"");
        txtNgayGH.setText(tblHoaDon.getValueAt(index, 6)+"");
        txtTienShip.setText(tblHoaDon.getValueAt(index, 7)+"");
        txtThanhTien.setText(tblHoaDon.getValueAt(index, 8)+"");
        TTHoaDon TTHD= new TTHoaDon();
        try {
            TTHD=TTHDD.selectById(String.valueOf( tblHoaDon.getValueAt(index, 1)));
        } catch (Exception ex) {
            Logger.getLogger(QLHoaDonJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        Combo.setSelectedItem(TTHD);
    }//GEN-LAST:event_tblHoaDonMouseClicked

