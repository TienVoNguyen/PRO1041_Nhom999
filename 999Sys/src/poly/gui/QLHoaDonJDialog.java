    private void loadDataToCombobox() {
        this.Combo.removeAllElements();
        List<TTHoaDon> lst = new ArrayList<>();
        try {
            lst=this.TTHDD.selectAll();
            for (TTHoaDon tTHoaDon : lst) {
                Combo.addElement(tTHoaDon);
            }
        } catch (Exception ex) {
            Logger.getLogger(QLHoaDonJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadDataToTableModel() {
        this.Model.setRowCount(0);
        List<HoaDon> listHD= new ArrayList<>();
        try {
            listHD= this.HDD.selectAll();
            for (HoaDon HD : listHD) {
                this.Model.addRow(new Object[]{
                    HD.getMaHD(),
                    HD.getMaTT(),
                    HD.getMaNV(),
                    HD.getMaKH(),
                    HD.getNgayMua(),
                    HD.getGiamGia(),
                    HD.getNgayGiaoHang(),
                    HD.getTienShip(),
                    HD.getThanhTien()
                });
            }
        } catch (Exception ex) {
            Logger.getLogger(QLHoaDonJDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    private boolean CheckForm() throws HeadlessException {
        StringBuilder sb = new StringBuilder();
        if (txtMaHD.getText().equals("")) {
            sb.append("Bạn không được để trống mã hóa đơn\n");
        }
        if (txtMaNV.getText().equals("")) {
            sb.append("Bạn không được để trống mã nhân viên\n");
        }
        if (txtMaKH.getText().equals("")) {
            sb.append("Bạn không được để trống mã khách hàng\n");
        }
        if (txtNgayMua.getText().equals("")) {
            sb.append("Bạn không được để trống ngày mua\n");
        }
        if (txtGiamGia.getText().equals("")) {
            sb.append("Bạn không được để trống giảm giá\n");
        }
        if (txtNgayGH.getText().equals("")) {
            sb.append("Bạn không được để trống ngày giao hàng\n");
        }
        if (txtTienShip.getText().equals("")) {
            sb.append("Bạn không được để trống tiền ship\n");
        }
        if (txtThanhTien.getText().equals("")) {
            sb.append("Bạn không được để trống thành tiền\n");
        }
        if (sb.length() > 0) {
            JOptionPane.showMessageDialog(this, sb, "Lỗi", JOptionPane.ERROR_MESSAGE);
            return true;
        }
        return false;
        
    }

    
