    private boolean checkMa() {
        try {
            int ma = Integer.parseInt(txtDVT_MaDVT.getText());
            ArrayList<DonViTinh> list = dao_dvt.selectAll();
            for (DonViTinh dvt : list) {
                if (dvt.getMaDVT() == ma) {
                    return true;
                }
            }
            return false;
        } catch (Exception ex) {
            Messeger.showErrorDialog(this, "Lỗi chech mã", "Error!");
            ex.printStackTrace();
            return false;
        }
    }
