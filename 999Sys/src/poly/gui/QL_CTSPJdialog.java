    private void LoadDataToTableSize() {
        try {
            modelTBL_Size.setRowCount(0);
            ArrayList<Size> list = dao_size.selectAll();
            for (int i = 0; i < list.size(); i++) {
                Size get = list.get(i);
                if (get.isTrangThai()) {
                    Object[] rowData = new Object[]{
                        get.getMaSize(),
                        get.getTenSize()
                    };
                    modelTBL_Size.addRow(rowData);
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

