    private void Auto_HetHan_KhuyenMai() {
        for (int i = 0; i < tblqlkm.getRowCount(); i++) {
            try {
                String ngaykt = XDate.toString(XDate.toDate(
                        String.valueOf(tblqlkm.getValueAt(i, 5)), "dd/MM/yyyy"), "yyyy-MM-dd");
                Date date1 = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                Date date2 = sdf.parse(ngaykt);
                String relation;
                if (date1.before(date2)) // Hoặc  else if (date1.after(date2)== false)
                {
                    relation = "Trước";
                } else {
                    relation = "Sau";
                }
                if (relation.equals("Sau")) {
                    try {
                        kmDao.delete(String.valueOf(tblqlkm.getValueAt(i, 0)));
                        LoadDataToTable_KM();
                    } catch (Exception ex) {
                        ex.printStackTrace();
                    }
                }
            } catch (ParseException ex) {
                Logger.getLogger(QL_KhuyenMaiJDiaLog.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

    private boolean Validate_QLKhuyenMai() {
        StringBuilder x = new StringBuilder();
        if (XValidate.isEmpty(txtTenKhuyenMai)) {
            x.append("Không Để Trống Tên Khuyến Mại\n");
        } else if (XValidate.isEmpty(txtGiaTri)) {
            x.append("Không Để Trống Giá Trị Khuyến Mại\n");
        } else if (Double.parseDouble(txtGiaTri.getText()) < 0) {
            if (XValidate.focus_Errol(false, txtGiaTri)) {
                x.append("Giá Trị Phải Lớn Hơn 0 \n");
            }
        } else if (XValidate.isNotNumber_Double(txtGiaTri)) {
            x.append("Giá Trị Phải Nhập Số \n");
        } else if (XValidate.isEmpty(txtGiamToiDa)) {
            x.append("Không Để Trống Giảm Giá Tối Đa\n");
        } else if (XValidate.isNotNumber_Double(txtGiamToiDa)) {
            x.append("Giảm Giá Tối Đa Phải Nhập Số\n");
        } else if (radiovnd.isSelected() & !(Double.parseDouble(txtGiaTri.getText()) == Double.parseDouble(txtGiamToiDa.getText()))) {
            if (XValidate.focus_Errol(false, txtGiamToiDa)) {
                x.append("Khi Giảm Giá Bằng VND Giảm Giá Tối Đa Phải Bằng Giá Trị \n");
            }
        }  else if (!Validate_Day()) {
                x.append("Ngày Bất Đầu Không Được Lớn Hơn Ngày Kết Thúc \n");
        }
        else if (radio_AD_HoaDon.isSelected() & XValidate.isEmpty(txthdtoithieu)) {
            x.append("Vui Lòng Nhập Giá Trị Tối Thiểu Của Hóa Đơn\n");
        } else if (radiovnd.isSelected() == true & Double.parseDouble(txtGiaTri.getText()) < 10000) {
            if (XValidate.focus_Errol(false, txtGiaTri)) {
                x.append("Giảm Giá Phải Lớn Hơn Hoặc Bằng 10 Ngàn");
            }
        } else if (radioPT.isSelected() == true & Double.parseDouble(txtGiaTri.getText()) > 100 | Double.parseDouble(txtGiaTri.getText()) < 0) {
            if (XValidate.focus_Errol(false, txtGiaTri)) {
                x.append("Giá Trị Khi Giảm Giá Phần Trăm Chỉ Được Nhập Từ 0 đến 100\n");
            }
        }
        if (x.toString().length() > 0) {
            Messeger.alert(this, x.toString());
            return true;
        }

        return false;
    }

